"""
Merge Roboflow export + locally auto-labeled frames into one final dataset.

Output structure (YOLOv8):
    final_dataset/
    ├── data.yaml
    ├── train/images/  +  train/labels/
    ├── valid/images/  +  valid/labels/
    └── test/images/   +  test/labels/

Split: 70% train / 20% valid / 10% test

Usage:
    python scripts/merge_datasets.py
"""
import random
import shutil
from pathlib import Path

# ==================================================================
# CONFIG
# ==================================================================
PROJECT_ROOT = Path(__file__).resolve().parent.parent

ROBOFLOW_EXPORT = Path("/Users/sertacakalin/Desktop/istanbul-traffic-vehicles")
AUTO_LABELED_ROOT = PROJECT_ROOT / "data/datasets/auto_labeled"
FINAL_DATASET = PROJECT_ROOT / "data/datasets/final_v3"

CLASSES = ["bus", "car", "motorcycle", "truck"]
TRAIN_RATIO = 0.70
VALID_RATIO = 0.20
TEST_RATIO = 0.10

SEED = 42
# ==================================================================


def collect_pairs():
    """Collect (image_path, label_path, source) tuples from all sources."""
    pairs = []

    # Source 1: Roboflow export (already split, but we'll re-split)
    rf_train_imgs = ROBOFLOW_EXPORT / "train/images"
    rf_train_lbls = ROBOFLOW_EXPORT / "train/labels"
    if rf_train_imgs.exists():
        for img in sorted(rf_train_imgs.glob("*.jpg")):
            lbl = rf_train_lbls / f"{img.stem}.txt"
            if lbl.exists():
                pairs.append((img, lbl, "roboflow"))
    print(f"  Roboflow: {sum(1 for p in pairs if p[2] == 'roboflow')} pairs")

    # Source 2: Local auto-labeled (4 folders)
    for folder_name in ["file1_night", "file2_dense", "file3_day", "file4_evening"]:
        folder = AUTO_LABELED_ROOT / folder_name
        imgs_dir = folder / "images"
        lbls_dir = folder / "labels"
        if not imgs_dir.exists():
            print(f"  WARN: {folder_name} not found, skipping")
            continue
        count = 0
        for img in sorted(imgs_dir.glob("*.jpg")):
            lbl = lbls_dir / f"{img.stem}.txt"
            if lbl.exists():
                pairs.append((img, lbl, folder_name))
                count += 1
        print(f"  {folder_name}: {count} pairs")

    return pairs


def split_pairs(pairs):
    """Random shuffle + 70/20/10 split."""
    random.seed(SEED)
    random.shuffle(pairs)
    n = len(pairs)
    n_train = int(n * TRAIN_RATIO)
    n_valid = int(n * VALID_RATIO)
    return {
        "train": pairs[:n_train],
        "valid": pairs[n_train:n_train + n_valid],
        "test": pairs[n_train + n_valid:],
    }


def copy_split(split_name, items, target_root):
    img_dir = target_root / split_name / "images"
    lbl_dir = target_root / split_name / "labels"
    img_dir.mkdir(parents=True, exist_ok=True)
    lbl_dir.mkdir(parents=True, exist_ok=True)

    for img, lbl, source in items:
        # Use unique name to avoid collisions: {source}_{original_name}
        new_name = f"{source}_{img.name}"
        shutil.copy(img, img_dir / new_name)
        shutil.copy(lbl, lbl_dir / f"{Path(new_name).stem}.txt")


def write_data_yaml(target_root):
    yaml_content = f"""# Final dataset: Roboflow export + locally auto-labeled CCTV
# Generated by scripts/merge_datasets.py

train: {target_root}/train/images
val:   {target_root}/valid/images
test:  {target_root}/test/images

nc: {len(CLASSES)}
names: {CLASSES}
"""
    (target_root / "data.yaml").write_text(yaml_content)


def main():
    print("=" * 60)
    print("MERGE DATASETS")
    print("=" * 60)
    print()
    print("Collecting source pairs...")
    pairs = collect_pairs()
    print(f"\nTotal pairs collected: {len(pairs)}")
    print()

    print("Splitting 70/20/10...")
    splits = split_pairs(pairs)
    for name, items in splits.items():
        print(f"  {name}: {len(items)} pairs")
    print()

    if FINAL_DATASET.exists():
        print(f"Removing existing {FINAL_DATASET}...")
        shutil.rmtree(FINAL_DATASET)

    print(f"Writing to {FINAL_DATASET}...")
    for split_name, items in splits.items():
        print(f"  Copying {split_name} ({len(items)} files)...")
        copy_split(split_name, items, FINAL_DATASET)

    write_data_yaml(FINAL_DATASET)

    print()
    print("=" * 60)
    print("DONE")
    print("=" * 60)
    print(f"Output: {FINAL_DATASET}")
    print(f"Train:  {len(splits['train'])} images")
    print(f"Valid:  {len(splits['valid'])} images")
    print(f"Test:   {len(splits['test'])} images")
    print(f"Classes: {CLASSES}")


if __name__ == "__main__":
    main()
