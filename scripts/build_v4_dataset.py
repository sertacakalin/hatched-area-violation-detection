"""
v4 dataset olustur — Roboflow + manuel dogrulanmis cam10/cam11.

Surec:
  1. cleaned/cam10/ ve cleaned/cam11/ icin images/ symlink'leri kur
     (Label Studio "YOLO" export'u images/ klasorunu bos birakir;
      images zaten auto_labeled/'da, oraya isaret edelim)
  2. Tum kaynaklardan etiketli image path'lerini topla
  3. %70 train / %20 valid / %10 test split (seed=42, reproducible)
  4. final_v4/{data.yaml, train.txt, valid.txt, test.txt} yaz

Cikti:
    data/datasets/final_v4/
"""
import random
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parent.parent
ROBOFLOW = Path("/Users/sertacakalin/Desktop/istanbul-traffic-vehicles")
AUTO = PROJECT_ROOT / "data/datasets/auto_labeled"
CLEANED = PROJECT_ROOT / "data/datasets/cleaned"
OUT = PROJECT_ROOT / "data/datasets/final_v4"

CLASSES = ["bus", "car", "motorcycle", "truck"]
SEED = 42
SPLIT_TRAIN, SPLIT_VAL = 0.70, 0.20  # geri kalan test


def link_cleaned_images() -> None:
    """cleaned/<cam>/images/ icindeki LS export'larini auto_labeled'a symlink'le."""
    for cam_tag, auto_folder in [
        ("cam10", "file5_cam10"),
        ("cam11", "file6_cam11"),
    ]:
        src = AUTO / auto_folder / "images"
        dst = CLEANED / cam_tag / "images"
        dst.mkdir(parents=True, exist_ok=True)

        linked = 0
        for img in src.glob("*.jpg"):
            target = dst / img.name
            if not target.exists():
                target.symlink_to(img.resolve())
                linked += 1
        print(f"  {cam_tag}: {linked} yeni symlink ({len(list(dst.glob('*.jpg')))} toplam)")


def collect_paths() -> list[str]:
    """Etiket dosyasi olan tum image path'lerini topla."""
    images = []

    # 1) Roboflow — train klasorundeki her image icin label varsa al
    rb_count = 0
    for img in (ROBOFLOW / "train/images").glob("*.jpg"):
        if (ROBOFLOW / "train/labels" / f"{img.stem}.txt").exists():
            images.append(str(img.resolve()))
            rb_count += 1
    print(f"  Roboflow: {rb_count} image")

    # 2) cleaned cam10 / cam11 (manuel dogrulanmis)
    for cam_tag in ["cam10", "cam11"]:
        cnt = 0
        for img in (CLEANED / cam_tag / "images").glob("*.jpg"):
            if (CLEANED / cam_tag / "labels" / f"{img.stem}.txt").exists():
                images.append(str(img.resolve()))
                cnt += 1
        print(f"  {cam_tag} (manuel): {cnt} image")

    # 3) v3 pseudo-labeled klasorler (file1_night, file3_day, file4_evening)
    for folder in ["file1_night", "file3_day", "file4_evening"]:
        cnt = 0
        for img in (AUTO / folder / "images").glob("*.jpg"):
            if (AUTO / folder / "labels" / f"{img.stem}.txt").exists():
                images.append(str(img.resolve()))
                cnt += 1
        print(f"  {folder} (pseudo): {cnt} image")

    return images


def write_yaml_and_splits(images: list[str]) -> None:
    """Shuffle + split + dosyalari yaz."""
    random.seed(SEED)
    random.shuffle(images)

    n = len(images)
    train_end = int(n * SPLIT_TRAIN)
    val_end = int(n * (SPLIT_TRAIN + SPLIT_VAL))

    splits = {
        "train": images[:train_end],
        "valid": images[train_end:val_end],
        "test": images[val_end:],
    }

    OUT.mkdir(parents=True, exist_ok=True)
    for name, paths in splits.items():
        (OUT / f"{name}.txt").write_text("\n".join(paths))
        print(f"  {name}.txt: {len(paths)} path")

    yaml_content = f"""# v4 final dataset (path-list format)
# - Roboflow (community labeled) + Label Studio (manually verified)
# - Generated by scripts/build_v4_dataset.py (seed={SEED})

path: {OUT}
train: train.txt
val:   valid.txt
test:  test.txt

nc: {len(CLASSES)}
names: {CLASSES}
"""
    (OUT / "data.yaml").write_text(yaml_content)
    print(f"\ndata.yaml -> {OUT / 'data.yaml'}")


def main():
    print("=== 1) Symlink images ===")
    link_cleaned_images()

    print("\n=== 2) Etiketli image'leri topla ===")
    images = collect_paths()
    print(f"\nTOPLAM: {len(images)} image")

    print("\n=== 3) Split + yaml yaz ===")
    write_yaml_and_splits(images)


if __name__ == "__main__":
    main()
